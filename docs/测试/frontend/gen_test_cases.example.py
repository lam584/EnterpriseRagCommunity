df = pd.DataFrame(data, columns=columns)

script_dir = os.path.dirname(os.path.abspath(__file__))
output_dir = os.path.join(script_dir, "temp")
os.makedirs(output_dir, exist_ok=True)
output_path = os.path.join(output_dir, "前台功能测试用例表.xlsx")

with pd.ExcelWriter(output_path, engine="openpyxl") as writer:
    df.to_excel(writer, index=False, sheet_name="前台功能测试")
    worksheet = writer.sheets["前台功能测试"]

    header_font = Font(bold=True, color="FFFFFF")
    header_fill = PatternFill("solid", fgColor="4F81BD")

    for row in worksheet.iter_rows(min_row=1, max_row=1):
        for cell in row:
            cell.font = header_font
            cell.fill = header_fill
            cell.alignment = Alignment(horizontal="center", vertical="center")

    for col_idx, _ in enumerate(columns, 1):
        column_letter = worksheet.cell(row=1, column=col_idx).column_letter
        if col_idx in [3, 4, 5, 6]:
            worksheet.column_dimensions[column_letter].width = 50
        elif col_idx == 2:
            worksheet.column_dimensions[column_letter].width = 24
        else:
            worksheet.column_dimensions[column_letter].width = 14

    for row in worksheet.iter_rows(min_row=2, max_row=len(data) + 1):
        for cell in row:
            cell.alignment = Alignment(wrap_text=True, vertical="top")

print(f"前台功能测试用例文件生成完毕: {os.path.abspath(output_path)}")
